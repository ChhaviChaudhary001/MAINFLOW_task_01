# String Manipulation
a = "Hey! What's up?"
b = "All is well. How about you?"

print(a.capitalize())  # Capitalize
print(a[5:])           # Slice
print(a.upper())       # Upper Case
print(a.lower())       # Lower Case
print(a.strip())       # Remove Whitespace
print(a.replace("W","H")) # Replace String
print(a.split("!"))    # Split String
print(a+" "+b)         # Concatination